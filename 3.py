def phi(m):
    # Ініціалізація результату
    result = m
    # Перевірка всіх чисел до sqrt(m) для знаходження простих дільників
    p = 2
    while p * p <= m:
        if m % p == 0:
            # Видалення всіх входжень простого дільника p
            while m % p == 0:
                m //= p
            # Використання формули Ейлера для простого дільника
            result -= result // p
        p += 1
    # Якщо m більше 1, воно є простим і впливає на результат
    if m > 1:
        result -= result // m
    return result

# Запит значення m у користувача
m = int(input("Введіть значення m: "))
print(f"Значення функції Ейлера для {m} дорівнює {phi(m)}")
