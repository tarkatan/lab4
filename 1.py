def gcdex(a, b):
    # Ініціалізація змінних для розширеного алгоритму Евкліда
    x0, x1, y0, y1 = 1, 0, 0, 1
    while b != 0:
        # Виконуємо ділення для отримання частки та залишку
        q, a, b = a // b, b, a % b
        # Оновлюємо значення x та y на основі частки
        x0, x1 = x1, x0 - q * x1
        y0, y1 = y1, y0 - q * y1
    # Повертаємо значення d = gcd, x = x0, y = y0
    return a, x0, y0

# Приклад для перевірки функції
a = 612
b = 342
d, x, y = gcdex(a, b)
print(f"Найбільший спільний дільник (d): {d}")
print(f"Коефіцієнт x: {x}")
print(f"Коефіцієнт y: {y}")
